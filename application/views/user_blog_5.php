            <li>Select an affiliate network and enter it.</li>
            <li>Choose which policies to support.</li>
            <li>Obtain a unique affiliate link for each bid.</li>
            <li>Post those links on your blog, social media account, or website.</li>
            <li>Collect a fee if anyone uses your links to make a purchase. </li>
        </ul>
        <p>Commission rates might vary according to the organization and the bid. You can earn either 5-10 percent of the amount or if you are lucky enough, the rate might escalate to 30-50 percent. You will also find some affiliate marketing programs which might include a flat rate per sale, instead of a percentage.&nbsp; </p>
